int
main
(
    int argc,
    char *argv[]
)
{

    try
    {
        cGame game; 
        
        // keep loopin'
        while (!cInterface::currentInterface->getKey (GLFW_KEY_ESCAPE) && 
               !cInterface::currentInterface->shouldClose() &&
               game.loopOnce ());

    }
    catch (...)
    {
        return 1;
    }

    return 0;
}
